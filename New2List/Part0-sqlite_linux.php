                <h1>SQLite-Installation unter Linux</h1>

                <h2>1. Installation von SQLite</h2>

                <h3>Ubuntu und Debian-basierte Distributionen</h3>
                <p>Für Ubuntu oder andere Debian-basierte Distributionen wie Linux Mint, installiere SQLite mit den folgenden Befehlen:</p>
                <pre><code>sudo apt-get update
sudo apt-get install sqlite3 libsqlite3-dev</code></pre>

                <h3>Fedora</h3>
                <p>Für Fedora-Nutzer installiere SQLite mit dem Befehl <code>dnf</code>:</p>
                <pre><code>sudo dnf install sqlite sqlite-devel</code></pre>

                <h3>Arch Linux</h3>
                <p>Unter Arch Linux oder darauf basierenden Distributionen wie Manjaro, installiere SQLite mit <code>pacman</code>:</p>
                <pre><code>sudo pacman -S sqlite</code></pre>

                <h3>openSUSE</h3>
                <p>Unter openSUSE verwende <code>zypper</code> zur Installation:</p>
                <pre><code>sudo zypper install sqlite3 sqlite3-devel</code></pre>

                <h3>CentOS/RHEL</h3>
                <p>Für CentOS oder RHEL-Distributionen verwende <code>yum</code> oder <code>dnf</code>:</p>
                <pre><code>sudo yum install sqlite sqlite-devel
# Oder unter neueren Versionen:
sudo dnf install sqlite sqlite-devel</code></pre>

                <h2>2. Überprüfen der Installation</h2>
                <p>Nachdem SQLite installiert ist, überprüfe die Version mit folgendem Befehl:</p>
                <pre><code>sqlite3 --version</code></pre>

                <h2>3. Erstellen einer SQLite-Datenbank</h2>
                <p>Um eine neue SQLite-Datenbank zu erstellen, gib einfach den folgenden Befehl ein:</p>
                <pre><code>sqlite3 todo.db</code></pre>
                <p>Das öffnet die SQLite-Shell, in der du SQL-Befehle direkt eingeben kannst.</p>

                <h2>4. Häufige Fallstricke</h2>
                <ul>
                    <li><strong>Rechteverwaltung:</strong> Achte darauf, dass der Webserver (z.B. Apache, Nginx) Schreibrechte auf die SQLite-Datenbankdatei hat.</li>
                    <li><strong>Datenbank-Sperrung:</strong> Bei gleichzeitigen Zugriffen kann SQLite eine Datenbank sperren. Stelle sicher, dass Transaktionen korrekt verwendet werden, um Deadlocks zu vermeiden.</li>
                    <li><strong>Datenbankgröße:</strong> SQLite eignet sich gut für kleine bis mittelgroße Anwendungen, wird jedoch bei sehr großen Datenbanken langsamer. Wenn die Datenbank zu groß wird, kann es ratsam sein, auf ein komplexeres Datenbankmanagementsystem (DBMS) wie MySQL oder PostgreSQL umzusteigen.</li>
                </ul>

                <h2>5. Verwendung von SQLite in PHP</h2>
                <p>Nachdem SQLite installiert wurde, kannst du es in PHP wie folgt verwenden:</p>
                <pre><code>$pdo = new PDO('sqlite:' . __DIR__ . '/todo.db');</code></pre>
                <p>Das <code>PDO</code>-Objekt ermöglicht dir die Durchführung von SQL-Abfragen und die Verbindung zu deiner SQLite-Datenbank.</p>

                <p>Zum Beispiel kannst du eine Tabelle erstellen:</p>
                <pre><code>$pdo->exec("CREATE TABLE IF NOT EXISTS todos (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    task TEXT NOT NULL,
    completed BOOLEAN NOT NULL DEFAULT 0
)");</code></pre>
